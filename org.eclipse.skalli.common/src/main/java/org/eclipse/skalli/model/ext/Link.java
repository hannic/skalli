package org.eclipse.skalli.model.ext;

import java.io.Serializable;

/*
 *  TODO refactoring / componentization
 *  This entity belongs to the 'LinkGroups' extension, but
 *  cannot reside in model.ext.misc as it is needed in model
 *  and view implementations.
 */
public class Link implements Serializable {

  private static final long serialVersionUID = -2158401882243718826L;
  private String label;
  private String url;

  public Link() {
    this(null, null);
  }

  public Link(String url, String label) {
    this.url = url;
    this.label = label;
  }

  public String getLabel() {
    return label;
  }

  public void setLabel(String label) {
    this.label = label;
  }

  public void setUrl(String url) {
    this.url = url;
  }

  public String getUrl() {
    return url;
  }

  @Override
  public int hashCode() {
    final int prime = 31;
    int result = 1;
    result = prime * result + ((url == null) ? 0 : url.hashCode());
    result = prime * result + ((label == null) ? 0 : label.hashCode());
    return result;
  }

  @Override
  public boolean equals(Object obj) {
    if (this == obj) {
      return true;
    }
    if (obj == null) {
      return false;
    }
    if (getClass() != obj.getClass()) {
      return false;
    }
    Link other = (Link) obj;
    if (url == null) {
      if (other.url != null) {
        return false;
      }
    } else if (!url.equals(other.url)) {
      return false;
    }
    if (label == null) {
      if (other.label != null) {
        return false;
      }
    } else if (!label.equals(other.label)) {
      return false;
    }
    return true;
  }

}

